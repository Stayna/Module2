using System;
public class Math
{
    public string myMath()
    {
        oper = Convert.ToChar(Console.ReadLine());
        {
            switch (oper)
            {
                case "+": a + b; return;
                case "-": (a - b); return;
                case "*": (a * b); return;
                case "/": (a / b); return;
            }
        }
    }
}
